def add(a,b):
    return a+b
def sub(a,b):
    return a-b
def mul(a,b):
    return a*b
def div(a,b):
    return a/b
x=int(input("Enter a number:"))
y=int(input("Enter another number:"))
print("Addition is:", add(x,y))
print("Subtraction is:", sub(x,y))
print("Multiplication is:", mul(x,y))
print("Division is:", div(x,y))